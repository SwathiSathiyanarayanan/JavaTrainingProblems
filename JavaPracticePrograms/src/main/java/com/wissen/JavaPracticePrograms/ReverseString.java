package com.wissen.JavaPracticePrograms;

public class ReverseString {

	public static void main(String[] args) {
		String s="Swathi";
		
		for(int i=s.length();i>0;i--) {
			System.out.println(s.charAt(i-1));
		}
		
		for(int i=s.length();i>0;i--) {
			System.out.println(s.charAt(i-1));
		}
	}
}
